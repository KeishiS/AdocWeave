import { readFileSync, writeFileSync } from "node:fs";
import process from "node:process";

const root = new URL("../", import.meta.url);
const schema = JSON.parse(readFileSync(new URL("protocol/public-api.json", root), "utf8"));
const names = schema.products.map((field) => field.json);
const rustFields = schema.products.map((field) => field.rust);
const header = "// @generated by tools/generate-protocol.mjs from protocol/public-api.json; DO NOT EDIT.\n";
const mjs = `${header}export const PROTOCOL_SCHEMA_VERSION = ${schema.schemaVersion};\nexport const PACKAGE_VERSION = ${JSON.stringify(schema.packageVersion)};\nexport const PRODUCT_FIELDS = ${JSON.stringify(names)};\n`;
const dts = `${header}export interface ProductSet {\n${names.map((name) => `  ${name}?: boolean;`).join("\n")}\n}\n\nexport declare const PROTOCOL_SCHEMA_VERSION: ${schema.schemaVersion};\nexport declare const PACKAGE_VERSION: ${JSON.stringify(schema.packageVersion)};\nexport declare const PRODUCT_FIELDS: readonly [${names.map(JSON.stringify).join(", ")}];\n`;
const rust = `${header}\n#[derive(Clone, Copy, Debug, serde::Deserialize, serde::Serialize, Eq, PartialEq)]\n#[serde(rename_all = "camelCase", deny_unknown_fields)]\npub struct WasmProductSet {\n${rustFields.map((name) => `    pub ${name}: bool,`).join("\n")}\n}\n\nimpl Default for WasmProductSet {\n    fn default() -> Self {\n        let products = adocweave::ProductSet::browser_default();\n        Self {\n${rustFields.map((name) => `            ${name}: products.${name},`).join("\n")}\n        }\n    }\n}\n\nimpl From<WasmProductSet> for adocweave::ProductSet {\n    fn from(value: WasmProductSet) -> Self {\n        Self {\n${rustFields.map((name) => `            ${name}: value.${name},`).join("\n")}\n        }\n    }\n}\n`;
const outputs = [
  [new URL("web-worker/protocol.generated.mjs", root), mjs],
  [new URL("web-worker/protocol.generated.d.mts", root), dts],
  [new URL("crates/adocweave-wasm/src/protocol_generated.rs", root), rust],
];
const check = process.argv.includes("--check");
let stale = false;
for (const [path, content] of outputs) {
  let actual = "";
  try { actual = readFileSync(path, "utf8"); } catch {}
  if (actual === content) continue;
  stale = true;
  if (!check) writeFileSync(path, content);
}
if (check && stale) throw new Error("generated protocol files are stale; run node tools/generate-protocol.mjs");
